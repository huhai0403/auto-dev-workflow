export function buildFinalReport(state: {
  workflowId: string;
  workflowType: "planning" | "pipeline";
  requirementDescription: string;
  completedSteps: string[];
  skippedSteps: string[];
  totalDurationMs: number;
  totalTokens: number;
  outputDir: string;
  mode: string;
  batchName?: string;
  lastError?: string;
}): string {
  return `# BMAD Workflow Final Report

| Field | Value |
|-------|-------|
| Workflow ID | ${state.workflowId} |
| Workflow Type | ${state.workflowType} |
| Mode | ${state.mode} |
| Output Directory | ${state.outputDir} |
| Batch | ${state.batchName ?? "n/a"} |
| Total Duration | ${Math.round(state.totalDurationMs / 1000)}s |
| Token Estimate | ~${state.totalTokens} |

## Requirement

${state.requirementDescription}

## Completed Steps

${state.completedSteps.map((s) => `- ✅ ${s}`).join("\n") || "- (none)"}

## Skipped Steps

${state.skippedSteps.map((s) => `- ⏭ ${s}`).join("\n") || "- (none)"}

## Artifacts

See output directory for generated planning/pipeline artifacts.

${state.lastError ? `## Last Error\n\n${state.lastError}` : ""}

---
*Generated by @huhai0403/bmad-workflow-mcp*
`;
}
