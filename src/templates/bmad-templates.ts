/**
 * Embedded BMAD-format templates and rules.
 * Logic dependency on BMAD-METHOD — no external skill files required.
 */

export const BMAD_RULES = {
  prdSections: [
    "Executive Summary",
    "Problem Statement",
    "Goals & Success Metrics",
    "User Personas",
    "Functional Requirements",
    "Non-Functional Requirements",
    "Out of Scope",
    "Open Questions",
  ],
  userStoryFormat: "As a {role}, I want {action}, so that {benefit}.",
  acceptanceCriteriaFormat: "Given {context}, When {action}, Then {outcome}.",
  storyPriority: ["P0", "P1", "P2", "P3"],
} as const;

export function buildPrd(requirement: string, projectSlug: string): string {
  const date = new Date().toISOString().slice(0, 10);
  return `# Product Requirements Document: ${projectSlug}

> Generated by bmad-workflow-mcp | BMAD format | ${date}

## Executive Summary

${requirement}

## Problem Statement

Users need a solution that addresses: ${requirement}

## Goals & Success Metrics

| Goal | Metric | Target |
|------|--------|--------|
| Deliver core capability | Feature completion | 100% MVP scope |
| Quality | Defect rate post-release | < 5 critical bugs |
| Usability | Task success rate | ≥ 90% |

## User Personas

### Primary User
- **Role**: End user / developer
- **Need**: ${requirement}
- **Pain**: Manual, inconsistent workflow without structured planning

## Functional Requirements

### FR-1: Core Feature
- **Description**: Implement capability derived from requirement
- **Source**: User requirement input
- **Priority**: P0

### FR-2: Workflow Artifacts
- **Description**: Produce BMAD-aligned planning artifacts (PRD, stories, AC, architecture, tasks)
- **Priority**: P0

## Non-Functional Requirements

- **Performance**: Planning artifacts generated within seconds
- **Maintainability**: Markdown artifacts, version-control friendly
- **Traceability**: Each story links back to FR IDs

## Out of Scope

- Production deployment automation (unless explicitly requested)
- Third-party integrations not mentioned in requirement

## Open Questions

- [ ] Confirm target tech stack with stakeholder
- [ ] Confirm MVP vs full scope boundary
`;
}

