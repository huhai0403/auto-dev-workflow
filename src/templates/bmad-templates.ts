export function buildAcceptanceCriteria(): string {
  return `# Acceptance Criteria

> BDD Format (Given / When / Then) per BMAD-METHOD

## Story 1.1 — Core capability

### AC-1.1.1
**Given** the requirement has been captured in the PRD  
**When** the development team implements the core feature  
**Then** the feature satisfies FR-1 as described in the PRD

### AC-1.1.2
**Given** a user with valid access  
**When** they use the primary workflow  
**Then** the expected outcome is achieved without errors

---

## Story 1.2 — Planning artifacts

### AC-1.2.1
**Given** a completed workflow run  
**When** artifacts are inspected under \`.bmad-output/\`  
**Then** PRD, user stories, architecture, and tasks files exist

---

## Story 1.3 — Validation & quality

### AC-1.3.1
**Given** acceptance criteria for a story  
**When** verification is performed  
**Then** each AC can be marked pass/fail with evidence
`;
}

export function buildArchitecture(requirement: string): string {
  return `# Architecture Design Recommendations

> BMAD Planning Artifact | ${new Date().toISOString().slice(0, 10)}

## Context

Requirement: ${requirement}

## Option A — Monolithic Application (Recommended for MVP)

| Aspect | Choice |
|--------|--------|
| Frontend | React / Vue / vanilla per project |
| Backend | Node.js or Python API |
| Storage | PostgreSQL or SQLite |
| Pros | Fast to build, simple deploy |
| Cons | Scaling limits later |

## Option B — Modular Monolith

| Aspect | Choice |
|--------|--------|
| Structure | Domain modules with clear boundaries |
| Pros | Easier evolution to microservices |
| Cons | More upfront design |

## Option C — Serverless / Edge

| Aspect | Choice |
|--------|--------|
| Compute | Cloud functions |
| Pros | Pay-per-use, auto-scale |
| Cons | Cold start, vendor lock-in |

## Recommendation

Start with **Option A** unless non-functional requirements demand distributed architecture.

## Key Components

1. **API Layer** — REST or RPC endpoints
2. **Domain Layer** — Business rules from user stories
3. **Persistence** — Entity storage per FR mapping
4. **Integration** — External services as needed

## Risks

| Risk | Mitigation |
|------|------------|
| Scope creep | Strict MVP from PRD Out of Scope |
| Tech debt | Follow task breakdown sprint boundaries |
`;
}

